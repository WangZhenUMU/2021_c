//日本某地发生了一件谋杀案，警察通过排查确定杀人凶手必为4个嫌疑犯的一个。
//以下为4个嫌疑犯的供词 :
//A说：不是我。
//B说：是C。
//C说：是D。
//D说：C在胡说
//已知3个人说了真话，1个人说的是假话。
//现在请根据这些信息，写一个程序来确定到底谁是凶手。
#define _CRT_SECURE_NO_WARNINGS
#include<stdio.h>
int main()
{
	int a[4] = { 0 };
	for (int i = 0; i < 4; i++)//把每个人当作杀人凶手拉进局子进行比较,看与事实是否想同。
	{
		a[i] = 1;
//			A不是凶手		C是凶手		  D是凶手		D不是凶手
		if (((a[0] != 1) + (a[2] == 1) + (a[3] == 1) + (a[3] != 1))==3)
		{
			printf("%c是杀人凶手！\n", 'A' + i);
			break;
		}
		a[i] = 0;
	}
	return 0;
}